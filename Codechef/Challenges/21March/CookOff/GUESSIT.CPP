#include<bits/stdc++.h>
using namespace std;
#define ll long long

int main()
{
    ios_base::sync_with_stdio(0);
    cin.tie(0); cout.tie(0);

    int t; cin>>t;
    while(t--){
        for(ll k=1;k<=1000;k++){
            cout<<k*k<<endl;
            int x; cin>>x;
            if(x==1){
                break;
            }
        }

    }
}